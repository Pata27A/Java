// and, or, not son tres

// and &&, cuando se utiliza el operador debe evaluar los dos valores para obtener un valor definido
let mayor = true;
let suscripto = true;
//console.log(true && false);
//console.log(true && true);
console.log('operador and', mayor && suscripto);


// or, se expresa utilisando dos barras paralelas, hace devolver true si almenos uno es true igual en false 
console.log('operador or', mayor || suscripto);

//not, invierte el valor de la varible
console.log('operador not', !mayor);